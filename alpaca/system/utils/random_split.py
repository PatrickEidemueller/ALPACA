import math
import random
from typing import Union, Iterable


from alpaca.system.interfaces.dataset import Dataset, DataLoaderInterface


def random_split(
    dataset: Dataset,
    lengths: Union[Iterable[int], Iterable[float]],
    seed: int = None,
) -> tuple[Dataset]:
    """
    Randomly split a dataset into non-overlapping new datasets of given lengths.

    If a list of fractions that sum up to 1 is given,
    the lengths will be computed automatically as
    floor(frac * len(dataset)) for each fraction provided.

    After computing the lengths, if there are any remainders, 1 count will be
    distributed in round-robin fashion to the lengths
    until there are no remainders left.

    @param dataset : The dataset to split
    @param lengths : The relative / absolute lengths of the subsets
    @param seed : The random seed for reproducibility

    @returns : Tuple with the generated subsets
    """
    indices = len(dataset)

    if math.isclose(sum(lengths), 1) and sum(lengths) <= 1:
        subset_lengths: list[int] = []
        for i, frac in enumerate(lengths):
            if frac < 0 or frac > 1:
                raise ValueError(f"Fraction at index {i} is not between 0 and 1")
            n_items_in_split = int(
                math.floor(len(dataset) * frac)  # type: ignore[arg-type]
            )
            subset_lengths.append(n_items_in_split)
        # type: ignore[arg-type]
        remainder = len(dataset) - sum(subset_lengths)
        # add 1 to all the lengths in round-robin fashion until the remainder is 0
        for i in range(remainder):
            idx_to_add_at = i % len(subset_lengths)
            subset_lengths[idx_to_add_at] += 1
        lengths = subset_lengths
        for i, length in enumerate(lengths):
            if length == 0:
                print(
                    f"WARNING: random_split: Length of split at index {i} is 0. This might result in an empty dataset."
                )

    # Cannot verify that dataset is Sized
    if sum(lengths) != len(dataset):  # type: ignore[arg-type]
        raise ValueError(
            "Sum of input lengths does not equal the length of the input dataset!"
        )

    indices = list(range(len(dataset)))
    random.seed(seed)
    random.shuffle(indices)

    subsets = []
    offset = 0
    for length in lengths:
        subsets.append(dataset.index_select(indices[offset : offset + length]))
        offset += length

    return subsets
